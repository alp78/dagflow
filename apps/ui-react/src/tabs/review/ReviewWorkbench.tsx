  async function pollExportCompletion() {
    let attempts = 0;
    const poll = async () => {
      attempts++;
      try {
        // Check the pipeline run status directly
        const rid = runId;
        if (rid) {
          const status = await api.getPipelineExecutionStatus([rid]);
          const runs = (status.runs ?? []) as unknown as Array<Record<string, unknown>>;
          if (runs.length > 0) {
            setPipelineRuns(runs);
            const runStatus = String(runs[0].status ?? "");
            if (runStatus === "exported") {
              setApprovalState("exported");
              await reloadAfterPipeline();
              return;
            }
            if (["failed", "blocked"].includes(runStatus)) {
              setError("Export failed");
              return;
            }
          }
        }

        if (attempts > 60) {
          await reloadAfterPipeline();
          return;
        }

        setTimeout(() => void poll(), 3000);
      } catch {
        await reloadAfterPipeline();
      }
    };
    void poll();
  }

