type DagNode = { id: string; label: string; stepKey: string; stepKeys?: string[]; col: number; row: number; assetKeys?: string[]; groupName?: string; groups?: string[]; metadata?: Record<string, unknown> };
type DagEdge = [string, string];

// Generated by ops/scripts/generate_dag_topology.py — true to Dagster definitions
import dagTopology from "../../generated/dag_topology.json";

const _wf = (dagTopology as unknown as Record<string, { nodes: DagNode[]; edges: DagEdge[] }>)["security_shareholder"];
const DAG_NODES: DagNode[] = _wf.nodes;
const DAG_EDGES: DagEdge[] = _wf.edges;

// Identify export nodes: any node that comes after the review stage.
// We detect them as nodes whose group is validated_export, OR nodes
// that are only reachable from validated_export nodes.
const EXPORT_GROUP_NAMES = new Set(["validated_export"]);
const _exportDirect = new Set(DAG_NODES.filter((n) => EXPORT_GROUP_NAMES.has(n.groupName ?? "")).map((n) => n.id));
const EXPORT_NODE_IDS = new Set(_exportDirect);
// Also include nodes only fed by export nodes (e.g., csv_export)
for (const edge of DAG_EDGES) {
  if (_exportDirect.has(edge[0])) EXPORT_NODE_IDS.add(edge[1]);
}

function computeNodeStatuses(
  steps: Array<Record<string, unknown>>,
  pipelineStatus: string,
  capturing?: boolean,
  captureComplete?: boolean,
  sourcesReady?: boolean,
  approvalState?: string,
): Record<string, string> {
  const result: Record<string, string> = {};
  const isExported = pipelineStatus === "exported" || approvalState === "exported";
  const isApproved = approvalState === "approved";
  const isFinished = ["pending_review", "exported", "historical_loaded"].includes(pipelineStatus);
  const isRunning = pipelineStatus === "running" || pipelineStatus === "queued";

  const stepMap = new Map<string, string>();
  for (const s of steps) stepMap.set(String(s.step_key ?? ""), String(s.status ?? ""));

  const CAPTURE_GROUPS = new Set(["source_capture"]);
  const isCapture = (node: DagNode) => CAPTURE_GROUPS.has(node.groupName ?? "");

  const sortedNodes = [...DAG_NODES].sort((a, b) => a.col - b.col);
  for (const node of sortedNodes) {
    const allKeys = node.stepKeys ?? [node.stepKey];
    const matchedStatuses = allKeys.map((k) => stepMap.get(k)).filter(Boolean) as string[];
    const stepStatus = matchedStatuses.length > 0
      ? (matchedStatuses.includes("failed") ? "failed"
        : matchedStatuses.includes("running") ? "running"
        : matchedStatuses.every((s) => s === "succeeded") ? "succeeded"
        : matchedStatuses.includes("succeeded") ? "running"
        : matchedStatuses[0])
      : undefined;

    // ── Capture nodes: driven by UI capture state + step data ──
    if (isCapture(node)) {
      if (capturing) { result[node.id] = "running"; continue; }
      if (stepStatus === "succeeded" || stepStatus === "running" || stepStatus === "failed") {
        result[node.id] = stepStatus; continue;
      }
      if (captureComplete || sourcesReady || isFinished || isApproved || isExported || isRunning) {
        result[node.id] = "succeeded"; continue;
      }
      result[node.id] = "idle"; continue;
    }

    // ── Export nodes: driven purely by approval state ──
    if (EXPORT_NODE_IDS.has(node.id)) {
      if (isExported) { result[node.id] = "succeeded"; continue; }
      if (isApproved) { result[node.id] = "running"; continue; }
      result[node.id] = "idle"; continue;
    }

    // ���─ Middle nodes (transform, review): use step data directly ──
    if (isFinished || isApproved || isExported) {
      result[node.id] = "succeeded"; continue;
    }

    if (stepStatus === "succeeded") { result[node.id] = "succeeded"; continue; }
    if (stepStatus === "running") { result[node.id] = "running"; continue; }
    if (stepStatus === "failed") { result[node.id] = "failed"; continue; }

    // No step data — check if all upstream nodes succeeded
    if (isRunning) {
      const upstreamDone = DAG_EDGES
        .filter(([, to]) => to === node.id)
        .every(([from]) => result[from] === "succeeded");
      result[node.id] = upstreamDone ? "running" : "idle";
      continue;
    }

    result[node.id] = "idle";
  }

  return result;
}

function PipelineDag({ steps, pipelineStatus, capturing, captureComplete, sourcesReady, approvalState }: { steps: Array<Record<string, unknown>>; pipelineStatus: string; capturing?: boolean; captureComplete?: boolean; sourcesReady?: boolean; approvalState?: string }) {
  const [hoveredNode, setHoveredNode] = useState<string | null>(null);
  const hideTimer = useRef<ReturnType<typeof setTimeout> | null>(null);
  const wrapRef = useRef<HTMLDivElement>(null);
  const tooltipRef = useRef<HTMLDivElement>(null);

  function showNode(id: string) {
    if (hideTimer.current) { clearTimeout(hideTimer.current); hideTimer.current = null; }
    setHoveredNode(id);
  }
  function scheduleHide() {
    if (hideTimer.current) clearTimeout(hideTimer.current);
    hideTimer.current = setTimeout(() => setHoveredNode(null), 150);
  }
  function cancelHide() {
    if (hideTimer.current) { clearTimeout(hideTimer.current); hideTimer.current = null; }
  }

  const nodeW = 72, nodeH = 36, gapX = 32, gapY = 6, padX = 8, padY = 8;
  const maxCol = Math.max(...DAG_NODES.map((n) => n.col));
  const maxRow = Math.max(...DAG_NODES.map((n) => n.row));
  const svgW = padX * 2 + (maxCol + 1) * nodeW + maxCol * gapX;
  const svgH = padY * 2 + (maxRow + 1) * (nodeH + gapY);

  function nx(col: number) { return padX + col * (nodeW + gapX); }
  function ny(row: number) { return padY + row * (nodeH + gapY); }
  function cy(node: DagNode) { return ny(node.row) + nodeH / 2; }

  const statusColor: Record<string, string> = {
    idle: "var(--border-subtle)",
    pending: "var(--text-dim)",
    running: "var(--accent-blue)",
    succeeded: "var(--accent-green)",
    failed: "var(--accent-red)",
  };

  const statusBg: Record<string, string> = {
    idle: "transparent",
    pending: "rgba(90,95,106,0.08)",
    running: "rgba(79,143,247,0.12)",
    succeeded: "rgba(62,207,122,0.10)",
    failed: "rgba(239,107,107,0.12)",
  };

  const statusLabel: Record<string, string> = {
    idle: "Idle", pending: "Pending", running: "Running", succeeded: "Succeeded", failed: "Failed",
  };

  const statuses = computeNodeStatuses(steps, pipelineStatus, capturing, captureComplete, sourcesReady, approvalState);

  const stepByKey = new Map<string, Record<string, unknown>>();
  for (const s of steps) stepByKey.set(String(s.step_key ?? ""), s);

  const hoveredDagNode = hoveredNode ? DAG_NODES.find((n) => n.id === hoveredNode) : null;
  const hoveredSteps = hoveredDagNode
    ? (hoveredDagNode.stepKeys ?? [hoveredDagNode.stepKey]).map((k) => stepByKey.get(k)).filter(Boolean)
    : [];
  const hoveredStep = hoveredSteps.length > 0 ? hoveredSteps[0] : null;
  const hoveredStatus = hoveredDagNode ? (statuses[hoveredDagNode.id] ?? "idle") : "idle";

  type TipRow = { label: string; value: string; mono?: boolean; href?: string };
  type TipSection = { title: string; rows: TipRow[] };

  function fmtKey(k: string): string {
    return k.replace(/^dagflow_/, "").replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
  }

  function tooltipSections(): TipSection[] {
    if (!hoveredDagNode) return [];
    const sections: TipSection[] = [];

    // 1. Identity
    const identity: TipRow[] = [
      { label: "Op", value: hoveredDagNode.stepKey, mono: true },
    ];
    if (hoveredDagNode.groupName) identity.push({ label: "Group", value: hoveredDagNode.groupName });
    if (hoveredDagNode.assetKeys && hoveredDagNode.assetKeys.length > 0) {
      for (const ak of hoveredDagNode.assetKeys) {
        identity.push({ label: "Asset", value: ak, mono: true, href: `${dagsterBaseUrl}/assets/${encodeURIComponent(ak)}` });
      }
    }
    sections.push({ title: "Dagster", rows: identity });

    // 2. Asset metadata from definitions
    const nodeMeta = hoveredDagNode.metadata;
    if (nodeMeta && typeof nodeMeta === "object") {
      const metaRows: TipRow[] = [];
      for (const [k, v] of Object.entries(nodeMeta)) {
        if (v === null || v === undefined || v === "") continue;
        metaRows.push({ label: fmtKey(k), value: typeof v === "object" ? JSON.stringify(v) : String(v) });
      }
      if (metaRows.length > 0) sections.push({ title: "Metadata", rows: metaRows });
    }

    // 3. Runtime sub-steps
    for (const step of hoveredSteps) {
      if (!step) continue;
      const stepLabel = String(step.step_label ?? step.step_key ?? "Step");
      const stepRows: TipRow[] = [];
      if (step.started_at) stepRows.push({ label: "Started", value: new Date(String(step.started_at)).toLocaleString() });
      if (step.ended_at) stepRows.push({ label: "Ended", value: new Date(String(step.ended_at)).toLocaleString() });
      if (step.started_at && step.ended_at) {
        const ms = new Date(String(step.ended_at)).getTime() - new Date(String(step.started_at)).getTime();
        stepRows.push({ label: "Duration", value: ms < 1000 ? `${ms}ms` : `${(ms / 1000).toFixed(1)}s` });
      }
      const meta = step.metadata as Record<string, unknown> | undefined;
      if (meta && typeof meta === "object") {
        const skip = new Set(["stage"]);
        for (const [k, v] of Object.entries(meta)) {
          if (skip.has(k) || v === null || v === undefined || v === "") continue;
          const val = typeof v === "object" ? JSON.stringify(v) : String(v);
          const isPath = k.includes("path") || k.includes("key") || k.includes("checksum") || k.includes("file_id") || k.includes("url");
          const href = undefined;
          stepRows.push({ label: fmtKey(k), value: val, mono: isPath, href });
        }
      }
      if (stepRows.length > 0) sections.push({ title: stepLabel, rows: stepRows });
    }

    return sections;
  }

  // Viewport-aware tooltip positioning
  useEffect(() => {
    if (!tooltipRef.current || !wrapRef.current) return;
    const tip = tooltipRef.current;
    const wrap = wrapRef.current;
    const wrapRect = wrap.getBoundingClientRect();
    const tipRect = tip.getBoundingClientRect();

    // Clamp right edge
    if (tipRect.right > window.innerWidth - 8) {
      const overflow = tipRect.right - window.innerWidth + 12;
      tip.style.transform = `translateX(-${overflow}px)`;
    } else {
      tip.style.transform = "";
    }
    // Clamp bottom edge
    if (tipRect.bottom > window.innerHeight - 8) {
      const overflow = tipRect.bottom - window.innerHeight + 12;
      tip.style.marginTop = `-${overflow}px`;
    } else {
      tip.style.marginTop = "";
    }
  }, [hoveredNode]);

  const tipX = hoveredDagNode ? nx(hoveredDagNode.col) + nodeW + 8 : 0;
  const tipY = hoveredDagNode ? ny(hoveredDagNode.row) : 0;

  return (
    <div ref={wrapRef} style={{ position: "relative", display: "inline-block" }}>
      <svg width={svgW} height={svgH} style={{ display: "block" }}>
        {DAG_EDGES.map(([from, to]) => {
          const fromNode = DAG_NODES.find((n) => n.id === from)!;
          const toNode = DAG_NODES.find((n) => n.id === to)!;
          const x1 = nx(fromNode.col) + nodeW;
          const y1 = cy(fromNode);
          const x2 = nx(toNode.col);
          const y2 = cy(toNode);
          const edgeStatus = statuses[fromNode.id] ?? "idle";
          const color = statusColor[edgeStatus] ?? statusColor.idle;
          return (
            <path key={`${from}-${to}`}
              d={`M${x1},${y1} C${x1 + gapX / 2},${y1} ${x2 - gapX / 2},${y2} ${x2},${y2}`}
              fill="none" stroke={color} strokeWidth={1.5} />
          );
        })}
        {DAG_NODES.map((node) => {
          const x = nx(node.col);
          const y = ny(node.row);
          const status = statuses[node.id] ?? "idle";
          const color = statusColor[status] ?? statusColor.idle;
          const bg = statusBg[status] ?? "transparent";
          const lines = node.label.split("\n");
          return (
            <g key={node.id} style={{ cursor: "pointer" }}
              onMouseEnter={() => showNode(node.id)}
              onMouseLeave={scheduleHide}>
              <rect x={x} y={y} width={nodeW} height={nodeH} rx={5} ry={5}
                fill={bg} stroke={color} strokeWidth={status === "running" ? 2 : 1.5}>
                {status === "running" && (
                  <animate attributeName="opacity" values="1;0.35;1" dur="1.8s" repeatCount="indefinite" />
                )}
              </rect>
              <circle cx={x + 9} cy={y + nodeH / 2} r={3} fill={color}>
                {status === "running" && (
                  <animate attributeName="opacity" values="1;0.35;1" dur="1.8s" repeatCount="indefinite" />
                )}
              </circle>
              {lines.map((line, i) => (
                <text key={i} x={x + 17} y={y + nodeH / 2 + (i - (lines.length - 1) / 2) * 10}
                  fill="var(--text-secondary)" fontSize="8" fontFamily="Inter, sans-serif"
                  dominantBaseline="central" style={{ pointerEvents: "none" }}>
                  {line}
                </text>
              ))}
            </g>
          );
        })}
      </svg>

      {hoveredDagNode && (
        <div
          ref={tooltipRef}
          className="dag-tooltip"
          onMouseEnter={cancelHide}
          onMouseLeave={scheduleHide}
          style={{
            position: "absolute",
            top: tipY,
            left: tipX,
            zIndex: 50,
          }}
        >
          <div className="dag-tooltip__header">
            <span className="dag-tooltip__dot" style={{ background: statusColor[hoveredStatus] }} />
            <span className="dag-tooltip__title">{hoveredDagNode.label.replace("\n", " ")}</span>
            <span className="dag-tooltip__status" style={{ color: statusColor[hoveredStatus] }}>
              {statusLabel[hoveredStatus] ?? hoveredStatus}
            </span>
          </div>
          {tooltipSections().map((section) => (
            <div key={section.title} className="dag-tooltip__section">
              <div className="dag-tooltip__section-title">{section.title}</div>
              <dl className="dag-tooltip__meta">
                {section.rows.map(({ label, value, mono, href }, i) => (
                  <React.Fragment key={`${label}-${i}`}>
                    <dt>{label}</dt>
                    <dd className={mono ? "mono" : ""}>
                      {href ? <a href={href} target="_blank" rel="noopener noreferrer">{value}</a> : value}
                    </dd>
                  </React.Fragment>
                ))}
              </dl>
            </div>
          ))}
        </div>
      )}
    </div>
  );
}

