      setAvailableDates(dates.map((d) => String(d.business_date ?? d)).sort());
      setAvailableDateSet(new Set(dates.filter((d) => d.is_ready).map((d) => String(d.business_date))));
      setSnapshotOptions(snapshots as unknown as Array<Record<string, unknown>>);
